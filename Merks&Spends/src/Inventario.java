    private void btnBuscarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarNombreActionPerformed
        //1. obtener el valor del txt
        String nombretext = txtNombre.getText();

        //2.Validar txt vacio
        if(nombretext.isEmpty()){
            JOptionPane.showMessageDialog(this,"El campo es obligatorio","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        //3. Busqueda, llenado de la tabla
        try{
            ResultSet rs = arcrud.bucarporNombre(nombretext);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);

            if(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("id_articulo"),rs.getString("nombre"),rs.getString("c.nombre"),rs.getString("descripcion"),rs.getInt("existencia")});
                

            }else {
                JOptionPane.showMessageDialog(this, "No se encontró un articulo con ese nombre", "Error", JOptionPane.WARNING_MESSAGE);

            }
        }
        catch(SQLException e){
            System.out.println("Error al llenar la tabla: "+e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarNombreActionPerformed

