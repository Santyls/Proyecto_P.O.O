    private void btnInventarioPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInventarioPDFActionPerformed
        Document documento = new Document();
        
        try{
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/OneDrive/Escritorio/Reporte_Inventario.pdf"));
            documento.open();
            
            Paragraph titulo = new Paragraph("REPORTE DE INVENTARIO", FontFactory.getFont("Arial", 20, Font.BOLD));
            titulo.setAlignment(Element.ALIGN_CENTER);
            documento.add(titulo);
            documento.add(new Paragraph(" "));

            PdfPTable tabla = new PdfPTable(5);
            tabla.addCell("ID");
            tabla.addCell("Nombre");
            tabla.addCell("Categoria");
            tabla.addCell("Descripcion");
            tabla.addCell("Existencia");
            
            ResultSet rs = arcrud.reporteInventario();
            if(rs.next()){
                do{
                    tabla.addCell(rs.getString(1));
                    tabla.addCell(rs.getString(2));
                    tabla.addCell(rs.getString(3));
                    tabla.addCell(rs.getString(4));
                    tabla.addCell(rs.getString(5));
                }while(rs.next());
                documento.add(tabla);
            }
            documento.close(); // <- dentro del try
            JOptionPane.showMessageDialog(this, "Reporte creado");
        }catch(DocumentException | FileNotFoundException | SQLException e){
             System.out.println( "Error al generar PDF: " + e.getMessage());
        }
    }//GEN-LAST:event_btnInventarioPDFActionPerformed

