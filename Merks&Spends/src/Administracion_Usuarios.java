    private void btnBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIdActionPerformed
        //1. obtener el valor del txt
        String idtext = txtId.getText();
        
        //2.Validar txt vacio
        if(idtext.isEmpty()){
            JOptionPane.showMessageDialog(this,"El id es obligatorio","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        //3. Busqueda, llenado de la tabla 
        try{
            int id = Integer.parseInt(idtext);
            ResultSet rs = crud.bucarporID(id);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            
            if(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("id"),rs.getString("nombre"),rs.getString("apellidos"),rs.getString("correo"),rs.getString("contrasena"),rs.getString("d.nombre"),rs.getString("privilegio") });
                txtNombre.setText(rs.getString("nombre"));
                txtApellidos.setText(rs.getString("apellidos"));
                txtCorreo.setText(rs.getString("correo"));
                txtContrasena.setText(rs.getString("contrasena"));
                cbxDepartamento.setSelectedItem(rs.getString("d.nombre"));
                cbxPrivilegio.setSelectedItem(rs.getString("Privilegio"));
            }else {
             JOptionPane.showMessageDialog(this, "No se encontró un usuario con ese ID", "Error", JOptionPane.WARNING_MESSAGE);
             
         }
        }
        catch(SQLException e){
            System.out.println("Error al llenar la tabla: "+e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarIdActionPerformed

