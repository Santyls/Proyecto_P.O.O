    private void llenarTabla(){
        ResultSet todos = crud.obtenerTodos();
        DefaultTableModel modeloT = (DefaultTableModel) jTable1.getModel();
        modeloT.setRowCount(0);
        
        try{
            while(todos.next()){
                modeloT.addRow(new Object[]{todos.getInt("id"),todos.getString("nombre"),todos.getString("apellidos"),todos.getString("correo"),todos.getString("contrasena"),todos.getString("d.nombre"),todos.getString("privilegio") });
            }
        }
        catch(SQLException x){
            System.out.println("Error al llenar la tabla"+x.getMessage());
        }
    }
