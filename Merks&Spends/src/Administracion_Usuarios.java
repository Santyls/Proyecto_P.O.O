    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
         //1. Guardamos en variables lo que se escribe el usuario
        String idtext = txtId.getText();
        
        if(idtext.isEmpty()){
            JOptionPane.showMessageDialog(this,"No se selecciono ningun usuario","Error al editar usuario",JOptionPane.ERROR_MESSAGE);
            return;
        }
        //3. Mostrar confirmación antes de actualizar
        int conf = JOptionPane.showConfirmDialog(this, 
            "¿Estás seguro de que deseas eliminar este usuario? \n Se eliminaran todos los registros relacionados", 
            "Confirmar exterminación", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.QUESTION_MESSAGE);

        if (conf == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(idtext);
            arcrud.deleteArticuloIdUsuario(id);
            boolean status = crud.deleteUsuario(id);

        if (status) {
            JOptionPane.showMessageDialog(this, "Usuario eliminado con éxito en la base de datos", "Exterminación exitosa", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se elimino el usuario", "Error", JOptionPane.ERROR_MESSAGE);
        }
        limpiarcampos();
    }
    }//GEN-LAST:event_btnBajaActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //1. Guardamos en variables lo que se escribe el usuario
        String nom = txtNombre.getText();
        String ape = txtApellidos.getText();
        String cor = txtCorreo.getText();
        String con = txtContrasena.getText();
        int dep = cbxDepartamento.getSelectedIndex();
        int pri = cbxPrivilegio.getSelectedIndex();
        String idtext = txtId.getText();
        
        //2. validar que no existan vacios
        if(nom.isEmpty() || cor.isEmpty() || con.isEmpty()){
            JOptionPane.showMessageDialog(this,"Todos los campos son obligatorios","Error al editar usuario",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        //3. Mostrar confirmación antes de actualizar
        int conf = JOptionPane.showConfirmDialog(this, 
            "¿Estás seguro de que deseas editar este usuario?", 
            "Confirmar edición", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.QUESTION_MESSAGE);

        if (conf == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(idtext);
            boolean status = crud.updateUsuario(nom,ape, cor, con, (dep+1), (pri+1),id);

        if (status) {
            JOptionPane.showMessageDialog(this, "Usuario actualizado con éxito en la base de datos", "Edición exitosa", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se guardó", "Error", JOptionPane.ERROR_MESSAGE);
        }
        limpiarcampos();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

